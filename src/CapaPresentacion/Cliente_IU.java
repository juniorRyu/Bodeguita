    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try {
            if (txtDni.getText().length() > 0) {
                if (txtDatos.getText().length() > 0) {
                    if (txtDireccion.getText().length() > 0) {
                        if (txtCelular.getText().length() > 0) {

                            Cliente objeto_cliente = new Cliente();
                            ClienteBD objeto_clienteBD = new ClienteBD();

                            objeto_cliente.setCliRucDni(txtDni.getText().toUpperCase());
                            objeto_cliente.setCliDatos(txtDatos.getText().toUpperCase());
                            objeto_cliente.setCliDireccion(txtDireccion.getText().toUpperCase());
                            objeto_cliente.setCliCelular(txtCelular.getText().toUpperCase());
                            objeto_cliente.setCliCorreo(txtCorreo.getText().toUpperCase());

                            objeto_clienteBD.registrarCliente(objeto_cliente);

                            JOptionPane.showMessageDialog(null, "Todo OK...");

                            reportar();
                            limpiar();

                        } else {
                            JOptionPane.showMessageDialog(null, "Ingrese telefono");
                            txtCelular.requestFocus();
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Ingrese apellidos");
                        txtDireccion.requestFocus();
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Ingrese nombre");
                    txtDatos.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Ingrese dni");
                txtDni.requestFocus();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "error");
        }

    }//GEN-LAST:event_btnRegistrarActionPerformed

