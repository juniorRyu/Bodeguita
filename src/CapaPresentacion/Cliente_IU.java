    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        try {
            if (txtDni.getText().length() > 0) {
                int aviso = JOptionPane.showConfirmDialog(rootPane, "Estas seguro de eliminar");
                if (aviso == 0) {

                    ClienteBD objeto_clienteBD = new ClienteBD();

                    String dni = txtDni.getText();

                    objeto_clienteBD.eliminarCliente(dni);

                    reportar();
                    //limpiamos las cajas
                    limpiar();
                    txtDni.requestFocus();

                }
            } else {
                JOptionPane.showMessageDialog(null, "Se necesita el codigo", "Error", JOptionPane.INFORMATION_MESSAGE);
            }

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, e);

        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            if (txtDni.getText().length() > 0) {

                setCursor(new java.awt.Cursor(java.awt.Cursor.WAIT_CURSOR));
                String dni = txtDni.getText();
                ClienteBD objeto_clienteBD = new ClienteBD();
                DefaultTableModel tabla_temporal;

                tabla_temporal = objeto_clienteBD.buscarCliente(dni);

                if (tabla_temporal.getRowCount() > 0) {

                    tabla_reporte_cliente.setModel(tabla_temporal);

                    txtDatos.setText(tabla_temporal.getValueAt(0, 1).toString());
                    txtDireccion.setText(tabla_temporal.getValueAt(0, 2).toString());
                    txtCelular.setText(tabla_temporal.getValueAt(0, 3).toString());
                    txtCorreo.setText(tabla_temporal.getValueAt(0, 4).toString());

                } else {
                    JOptionPane.showMessageDialog(null, "no se encontro datos...");
                    txtDni.requestFocus();
                    txtDni.setText("");
                }

                setCursor(new java.awt.Cursor(java.awt.Cursor.DEFAULT_CURSOR));
            } else {
                JOptionPane.showMessageDialog(null, "Ingrese el dni");
                txtDni.requestFocus();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "error al buscar");
            setCursor(new java.awt.Cursor(java.awt.Cursor.DEFAULT_CURSOR));
        }
    }//GEN-LAST:event_btnBuscarActionPerformed


