    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        DefaultTableModel tabla_temporal_venta;

        int dia = dcFechaInicio.getCalendar().get(Calendar.DAY_OF_MONTH);
        int anio = dcFechaInicio.getCalendar().get(Calendar.YEAR);
        int mes = dcFechaInicio.getCalendar().get(Calendar.MONTH) + 1;
        String fechaInicio = anio + "-" + mes + "-" + dia;

        dia = dcFechaFinal.getCalendar().get(Calendar.DAY_OF_MONTH);
        anio = dcFechaFinal.getCalendar().get(Calendar.YEAR);
        mes = dcFechaFinal.getCalendar().get(Calendar.MONTH) + 1;
        String fechaFinal = anio + "-" + mes + "-" + dia;

        VentaBD objeto_ventaBD = new VentaBD();
        tabla_temporal_venta = objeto_ventaBD.reportarVentaFecha(fechaInicio, fechaFinal);
        tabla_reporte_venta.setModel(tabla_temporal_venta);

        int cantLista = tabla_temporal_venta.getRowCount();
        txtCantidadVentas.setText("" + cantLista);


    }//GEN-LAST:event_btnBuscarActionPerformed

