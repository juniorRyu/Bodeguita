    private void tabla_reporte_ventaMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabla_reporte_ventaMousePressed

        int fila_seleccionada = tabla_reporte_venta.getSelectedRow();
        
        DefaultTableModel tabla_temporal_venta = (DefaultTableModel) tabla_reporte_venta.getModel();
        
        int idventa=Integer.parseInt(tabla_temporal_venta.getValueAt(fila_seleccionada, 0).toString());
        
        DetalleVentaBD objeto_detalleVentaBD=new DetalleVentaBD();
        tabla_temporal_venta=objeto_detalleVentaBD.buscarDetalleVenta(idventa);
        
        tabla_reporte_detalle_venta.setModel(tabla_temporal_venta);
               
        int cantLista = tabla_temporal_venta.getRowCount();
        txtCantidadProductos.setText("" + cantLista);

    }//GEN-LAST:event_tabla_reporte_ventaMousePressed


